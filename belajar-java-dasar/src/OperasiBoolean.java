public class OperasiBoolean {
    public static void main(String[] args) {
        boolean trueValue = true;
        boolean falseValue = false;

        //And
        System.out.println(trueValue && falseValue);

        //Or
        System.out.println(trueValue || falseValue);

        //Kebalikan
        System.out.println(!falseValue);
    }
}
